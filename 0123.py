import turtle
t = turtle.Turtle()
turtle.title(" Quà Tặng Cậu ")
screen = turtle.Screen()
screen.bgcolor("Pink")
Style = ('Courier',30,'italic')
turtle.write(' Tên Bạn               Tên Crush',font=Style,align='center')
t.pensize(10)
t.color("red")
t.begin_fill()
t.fillcolor("red")
t.left(140)
t.forward(180)
t.circle(-90,200)
t.setheading(60)
t.circle(-90,200)
t.forward(180)
t.end_fill()
t.pensize(10)
t.screen.mainloop()







